const char* DCXDecoder::get_hazard_onset_tow_str(int tow) {
  const char weekstr[][16] = {
    "Monday",
    "Tuesday",
    "Wednesday",
    "Thursday",
    "Friday",
    "Saturday",
    "Sunday",
  };
  if (tow == 0) {
    return "";
  }
  static char buf[64];
  tow = tow - 1;
  int week = tow / (60 * 24);
  int hour = (tow / 60) % 24;
  int min = tow % 60;
  snprintf(buf, sizeof(buf), "%s %02d:%02d", weekstr[week], hour, min);
  return buf;
}

const char* DCXDecoder::get_hazard_onset_tow_str_ja(int tow) {
  const char weekstr[][16] = {
    "月曜日",
    "火曜日",
    "水曜日",
    "木曜日",
    "金曜日",
    "土曜日",
    "日曜日",
  };
  if (tow == 0) {
    return "";
  }
  static char buf[64];
  // JSTにする？
  // tow = tow - 1 + 9 * 60;
  // tow = tow % (24 * 60 * 7);
  tow -= 1;
  int week = tow / (60 * 24);
  int hour = (tow / 60) % 24;
  int min = tow % 60;
  snprintf(buf, sizeof(buf), "%s %02d:%02d UTC", weekstr[week], hour, min);
  return buf;
}

